package Wuzzuf;

public class POJO
{

    // Define attributes
     String title;
     String company;
     String location;
     String type;
     String level;
     String yearsExp;
     String country;
     String skills;

    // Define Constructor
    public POJO(String title, String company, String location, String type, String level, String yearsExp, String country, String skills) {
        this.title = title;
        this.company = company;
        this.location = location;
        this.type = type;
        this.level = level;
        this.yearsExp = yearsExp;
        this.country = country;
        this.skills = skills;
    }



}
